let Number1 = Number(prompt("Gib deine erste Zahl ein"));
let Number2 = Number(prompt("Gib deine zweite Zahl ein"));
let type = prompt("Gib die Rechenart ein");


if (type == "+") {
    let result = Number1 + Number2

    console.log(result)

} if (type == "-") {
    let result = Number1 - Number2

    console.log(result)

} if (type == "*") {
    let result = Number1 * Number2

    console.log(result)

} if (type == "/") {
    let result = Number1 / Number2

    console.log(result)
}

